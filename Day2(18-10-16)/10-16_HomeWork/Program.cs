using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _10_16_HomeWork
{
    class ForLoop
    {
        public static void Main(string[] args)
        {
            for (int i = 0; i < 3; i++)
            {
                int j = i;
                Console.WriteLine("i = {0} and j = {1}", i, j);
            }
        }
    }

    class ForLoop
    {
        public static void Main(string[] args)
        {
            int i = 1;
            for (;i<6;)
            {
                Console.WriteLine("C# For Loop: Iteration {0}", i);
                i++;
            }
        }
    }
}
